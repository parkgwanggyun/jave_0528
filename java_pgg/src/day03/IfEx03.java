package day03;

public class IfEx03 {

	public static void main(String[] args) {
		//else if예제
		int num = 0;
		//num가 0이면 0이라고 출력하고
		if(num == 0) {
			System.out.println("0입니다");
		}
		else if(num >= 1){
			System.out.println("양수입니다");
			
		}
		else {
			System.out.println("음수입니다.");
		}
		
		//num가 0이 아니고 양수이면 양수라고 출력하고
		
		//num가 0과 양수가 아니면 음수라고 출력하는 코드를 작성하세요

	}

}
